static void LCD_WR_DATA(uint16_t da) //zakladamy ze jest 16-bitowy
{
    HAL_GPIO_WritePin(GPIOA, LCD_AO, 1);
	LCD_Writ_Bus(da>>8);
	LCD_Writ_Bus(da);
}

static void LCD_WR_REG(char da)
{
    //LCD_AO=0;
	HAL_GPIO_WritePin(GPIOA, LCD_AO, 0);
	HAL_Delay(3);
	LCD_Writ_Bus(da);
}

static void LCD_WR_DATA8(char da)
{
    //LCD_AO=1;
	if (HAL_GPIO_ReadPin(GPIOA, LCD_AO) != GPIO_PIN_SET) {
		HAL_GPIO_WritePin(GPIOA, LCD_AO, 1);
		HAL_Delay(3);
	}
	LCD_Writ_Bus(da);
}

static void Address_set(unsigned int x1,unsigned int y1,unsigned int x2,unsigned int y2)
{
   LCD_WR_REG(0x2a);
   LCD_WR_DATA8(x1>>8);
   LCD_WR_DATA8(x1);
   LCD_WR_DATA8(x2>>8);
   LCD_WR_DATA8(x2);

   LCD_WR_REG(0x2b);
   LCD_WR_DATA8(y1>>8);
   LCD_WR_DATA8(y1);
   LCD_WR_DATA8(y2>>8);
   LCD_WR_DATA8(y2);

   LCD_WR_REG(0x2C);

}

static void LCD_DrawPoint(uint16_t x,uint16_t y)
{
	Address_set(x,y,x,y);
	LCD_WR_DATA(POINT_COLOR);
}

static void LCD_ShowChar(uint16_t x,uint16_t y,uint8_t num,uint8_t mode)
{
	uint8_t temp;
	uint8_t pos,t;
	uint16_t x0=x;
	uint16_t colortemp=POINT_COLOR;
    if(x>LCD_W-16||y>LCD_H-16)return;

	num=num-' ';
	Address_set(x,y,x+8-1,y+16-1);
	if(!mode)
	{
		for(pos=0;pos<16;pos++)
		{
			temp=asc2_1608[(uint16_t)num*16+pos];
			for(t=0;t<8;t++)
		    {
		        if(temp&0x01)POINT_COLOR=colortemp;
				else POINT_COLOR=BACK_COLOR;
				LCD_WR_DATA(POINT_COLOR);
				temp>>=1;
				x++;
		    }
			x=x0;
			y++;
		}
	}else
	{
		for(pos=0;pos<16;pos++)
		{
		    temp=asc2_1608[(uint16_t)num*16+pos];
			for(t=0;t<8;t++)
		    {
		        if(temp&0x01)LCD_DrawPoint(x+t,y+pos);//��һ����
		        temp>>=1;
		    }
		}
	}
	POINT_COLOR=colortemp;
}
