void LCD_Clear(uint16_t Color)
{
	uint16_t VH,VL;
	uint16_t i,j;
	VH=Color>>8;
	VL=Color;
	Address_set(0,0,LCD_W-1,LCD_H-1);
    for(i=0;i<LCD_W;i++)
	 {
	  for (j=0;j<LCD_H;j++)
	   	{
        	 LCD_WR_DATA8(VH);
			 LCD_WR_DATA8(VL);

	    }
	 }
}

uint8_t LCD_ShowString(uint16_t x,uint16_t y,const uint8_t *p)
{
	uint8_t size = 0;
    while(*p!='\0')
    {
    	size+=1;
        if(x>LCD_W-16){x=0;y+=16;}
        if(y>LCD_H-16){y=x=0;}
        LCD_ShowChar(x,y,*p,0);
        x+=8;
        p++;
    }
    return size;
}

void LCD_Clear_size(uint16_t Color, int x, int y, int x_delta)
{
	uint16_t VH,VL;
	uint16_t i,j;
	VH=Color>>8;
	VL=Color;
	int y_delta = 16;

	if(x+x_delta>LCD_W-16)
	{
		y_delta=16*(1+(x+x_delta)/(LCD_W-16));
		x=0;
		x_delta = LCD_W-1;
	}

	Address_set(x,y,x_delta+x,y_delta+y);
	for(i=0;i<=x_delta+1;i++)
	{
		for (j=0;j<=y_delta+1;j++)
		 {
			LCD_WR_DATA8(VH);
			LCD_WR_DATA8(VL);
		 }
	}
}
